Przypadki testowe opisane dla strony:     login.pracuj.pl

ID: 1
Tytuł: 
Pozytywne przejście procesu logowania

Priorytet: 
Wysoki

Opis:
Użytkownik loguje się za pomocą bezbłędnie wpisanego loginu i hasła.

Kroki do wykonania przypadku:
      1. Wejdź na stronę ‘login.pracuj.pl’.
      2. W polu "adres e-mail" wpisz poprawną nazwę użytkownika.
      3. W polu "hasło" wpisz poprawne hasło.
      4. Kliknij przycisk "OK".
      
Oczekiwany rezultat:
Użytkownik loguje się do aplikacji.

Warunki wstępne do wykonania testu:
Użytkownik musi być zarejestrowany na portalu pracuj.pl.

Typ testu:
Pozytywny (+)

Środowisko:
Windows 10, Chrome  80.0.3987.149
